    public Cliente() throws IOException {
        initComponents();   
        nombre = JOptionPane.showInputDialog("Introduce un nombre");
        this.setTitle(nombre);
        cliente = new Socket("localhost",6500);
        txtChat.setText("Te uniste a la sala");
        try {
            output = new DataOutputStream(cliente.getOutputStream());
            input = new DataInputStream(cliente.getInputStream());
            output.writeUTF(nombre+" se uni√≥ a la sala de chat");
            output.flush();
        } catch (IOException ex) {
            
        }
        
        HiloRecibirMensaje hilo = new HiloRecibirMensaje(this,input);
        hilo.start();
        
        
    }

